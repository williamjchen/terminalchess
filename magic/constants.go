// bitboard layout
// 56 57 58 59 60 61 62 63
// 48 49 50 51 52 53 54 55
// 40 41 42 43 44 45 46 47 
// 32 33 34 35 36 37 38 39 
// 24 25 26 27 28 29 30 31 
// 16 17 18 19 20 21 22 23 
// 08 09 10 11 12 13 14 15 
// 00 01 02 03 04 05 06 07
// ^ corresponds to rank and file index at 1
// 8
// 7
// ... (files)
// 3
// 2
// 1  2  3  4  5  6  7  8 (rank)
// A  B  C  D  E  F  G  H

// Compass for bitboard
// <<7 <<8 <<9
// >>1  0  <<1
// >>9 >>8 >>7

import (
	"math/bits"
	"log/slog"
)

// Square index values from 0-63.
type Square uint8

func Init() {
	slog.Info("Generating magic bitboards....")
	MagicMovesRook = make([][]uint64, 64, 64)
	MagicMovesBishop = make([][]uint64, 64, 64)
	for i := 0; i < 64; i++ {
		MagicMovesRook[i] = make([]uint64, magicDbSizeRook[i], magicDbSizeRook[i]) // 2^n possible blocker permutations
		MagicMovesBishop[i] = make([]uint64, magicDbSizeBishop[i], magicDbSizeBishop[i]) // 2^n possible blocker permuations
	}
	generateRookMagicTable()
	generateBishopMagicTable()
}

func generateRookMagicTable() {
	// For a rook at every board position
	for i := 0; i < 64; i++ {
		blockerMask := MagicRookBlockerMasks[i]
		generateRookBlockerBoardPermutations(Square(i), blockerMask, 0)
	}
}

func generateBishopMagicTable() {
	// For a bishop at every board position
	for i := 0; i < 64; i++ {
		blockerMask := MagicBishopBlockerMasks[i]
		generateBishopBlockerBoardPermutations(Square(i), blockerMask, 0)
	}
}

// eliminates least significant bit, and calls 2 recursive functions with and without that bit. O(2^n)
func generateRookBlockerBoardPermutations(origin Square, blockerMask uint64, cur uint64) {
	if blockerMask == 0 {
		magicIndex := RookHash(origin, cur)
		MagicMovesRook[origin][magicIndex] = rookMoves(origin, cur)
		return
	}

	generateRookBlockerBoardPermutations(origin, blockerMask & (blockerMask - 1), cur | 1 << bits.TrailingZeros64(blockerMask)) // with
	generateRookBlockerBoardPermutations(origin, blockerMask & (blockerMask - 1), cur) // without
}

func generateBishopBlockerBoardPermutations(origin Square, blockerMask uint64, cur uint64) {
	if blockerMask == 0 {
		magicIndex := BishopHash(origin, cur)
		MagicMovesBishop[origin][magicIndex] = bishopMoves(origin, cur)
		return
	}

	generateBishopBlockerBoardPermutations(origin, blockerMask & (blockerMask - 1), cur | 1 << bits.TrailingZeros64(blockerMask)) // with
	generateBishopBlockerBoardPermutations(origin, blockerMask & (blockerMask - 1), cur) // without
}

func rookMoves(origin Square, blockers uint64) uint64 {
	var moves uint64 = 0
	var start uint64 = 1 << origin
	// up
	move := start << 8
	for move != 0 {
		moves |= move

		// checks if move is occupied. Notice that we already include the move before we check.
		// This is because we can capture that piece. But we ignore the case of capturing our own piece for now.
		if blockers & ^move != blockers {
			break
		}
		move <<= 8
	}

	// down
	move = start >> 8
	for move != 0 {
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move >>= 8
	}

	// right
	move = start << 1
	for bits.TrailingZeros64(move) < ((int(origin) / 8) + 1) * 8 && move != 0 {
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move <<= 1
	}

	// left
	move = start >> 1
	for bits.TrailingZeros64(move) >= (int(origin) / 8) * 8 && move != 0{
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move >>= 1
	}
	
	return moves
}

func bishopMoves(origin Square, blockers uint64) uint64 {
	var moves uint64 = 0
	var start uint64 = 1 << origin

	// NE
	move := start << 9
	for move != 0 {
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move <<= 9
	}

	// NW
	move = start << 7
	for move != 0 {
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move <<= 7
	}

	// SE
	move = start >> 7
	for move != 0 {
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move >>= 7
	}

	// SW
	move = start >> 9
	for move != 0 {
		moves |= move

		if blockers & ^move != blockers {
			break
		}
		move >>= 9
	}
	
	return moves
}

func RookHash(origin Square, blockers uint64) uint64 {
	return (blockers >> magicNumberRook[origin]) >> magicRookShifts[origin] // (blockers * magic) >> (64 - index_bits)
}

func BishopHash(origin Square, blockers uint64) uint64 {
	return (blockers >> magicNumberBishop[origin]) >> magicBishopShifts[origin] // (blockers * magic) >> (64 - index_bits)
}

